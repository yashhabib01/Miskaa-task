package com.example.miskaa_task.objects;

public class Translation {


    private  String br;

    private  String de;

    private  String es;

    private  String fa;

    private  String fr;

    private  String hr;

    private  String it;

    private  String ja;

    private  String nl;

    private  String pt;


    public Translation(String br, String de, String es, String fa, String fr, String hr, String it, String ja, String nl, String pt) {
        this.br = br;
        this.de = de;
        this.es = es;
        this.fa = fa;
        this.fr = fr;
        this.hr = hr;
        this.it = it;
        this.ja = ja;
        this.nl = nl;
        this.pt = pt;
    }

    public  String getBr() {
        return this.br;
    }


    public  String getDe() {
        return this.de;
    }

    public  String getEs() {
        return this.es;
    }


    public  String getFa() {
        return this.fa;
    }


    public  String getFr() {
        return this.fr;
    }


    public  String getHr() {
        return this.hr;
    }

    public  String getIt() {
        return this.it;
    }


    public  String getJa() {
        return this.ja;
    }


    public  String getNl() {
        return this.nl;
    }


    public  String getPt() {
        return this.pt;
    }



}
